import Head from "next/head";
import Banner from "../components/Banner";
import Footer from "../components/Footer";
import Header from "../components/Header";
import LargeCard from "../components/LargeCard";
import MediumCard from "../components/MediumCard";
import SmallCard from "../components/SmallCard";
import { ICard, IProperty } from "../typings";

interface IProps {
  exploreData: IProperty[];
  cardsData: ICard[];
}

const Home = ({ exploreData, cardsData }: IProps) => {
  return (
    <div className="">
      <Head>
        <title>ScareBnb</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Header placeholder="" />
      <Banner />

      <main className="px-8 mx-auto max-w-7xl sm:px-16">
        <section className="pt-6">
          <h2 className="pb-5 text-4xl font-semibold">Explore Nearby</h2>
          <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4">
            {exploreData?.map((property) => (
              <SmallCard property={property} key={property.img} />
            ))}
          </div>
        </section>

        <section>
          <h2 className="py-8 text-4xl font-semibold">Stay Anywhere</h2>

          <div className="flex p-3 -ml-3 space-x-3 overflow-scroll scrollbar-hide">
            {/* destructure out img and title first */}
            {cardsData?.map(({ img, title }, index) => (
              <MediumCard key={index} img={img} title={title} />
            ))}
          </div>
        </section>

        <LargeCard
          img="/large-card.avif"
          title="The Greatest Outdoors"
          description="Wishlists curated by ScareBnb"
          buttonText="Get Inspired"
        />
      </main>
      <Footer />
    </div>
  );
};

export const getStaticProps = async () => {
  const exploreData = await fetch("https://www.jsonkeeper.com/b/5V1F").then(
    (res) => res.json()
  );

  const cardsData = await fetch("https://www.jsonkeeper.com/b/LRQM").then(
    (res) => res.json()
  );

  return {
    props: {
      exploreData,
      cardsData,
    },
  };
};

export default Home;
